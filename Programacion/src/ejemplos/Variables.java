package ejemplos;
public class Variables{
	public static void main (String[] args){
		//Creo la variable llamada variable1 que es de tipo entero
		int variable1;
		//Asigno un valor a la variable1
		variable1=10;
		//Imprimo variable1
		System.out.println(variable1);
		
		//Creación de variable asignando valor
		int var2 = 20;
		//Creo la variable var3 y le asigno la suma de las anteriores
		int var3 = variable1 + var2;
		var3=80;
		System.out.println(var3);
		//Puedo crear varias variables en la misma línea
		int var4, pepe, juan=8;
		
		int a=250;
		//Casting: Consiste en transformar explicitamente una variable en otra de otro tipo
		//Para hacer casting se pone el tipo de variable entre parentesis delante del valor.
		byte b=(byte)a;
		System.out.println(b);
		
		//En una variable de tipo char puedo meter un caracter. Hay que usar la comilla simple
		char letra = 'a';
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	
	}
}